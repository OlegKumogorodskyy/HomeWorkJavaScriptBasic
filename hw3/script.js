let fingerNumber = prompt("Enter the finger number on your hand (from 1 to 5):");

if (fingerNumber === '1') {
    alert("Thumb");
} else if (fingerNumber === '2') {
    alert("Index finger");
} else if (fingerNumber === '3') {
    alert("Middle finger");
} else if (fingerNumber === '4') {
    alert("Ring finger");
} else if (fingerNumber === '5') {
    alert("Pinky");
} 
